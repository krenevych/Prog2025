# Для заданого натурального числа n виведіть
# його найменший дільник, відмінний від 1.

n = int(input())

# for i in range(2, n + 1):

for i in range(2, int(n**0.5) + 1): # оптимізуємо
    if n % i == 0:
        print(i)
        break
else:  # сюди ми потрапляємо, якщо не спрацював break
    print(n)
