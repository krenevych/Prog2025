# Знайти суму парних чисел з діапазону від 1 до N.
N = int(input("N=? " ))
# 2, 4, 6, ... N-1/N (залежно від того чи парне N)
suma = 0
for i in range(2, N+1, 2):
    # print(i, end=", ")  # інформація для дебагу, щоб спростити розуміння чи правильно працює програма

    suma += i # suma = suma + i

print()
print("suma = ", suma)