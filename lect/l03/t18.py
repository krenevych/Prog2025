
# Задано послідовність чисел, що завершується числом 0.
# Потрібно знайти найменше серед додатних чисел.

# 1, -43, 23, 12, -222, -67, 0

min = 100500
while True: # нескінченний цикл, завершення роботи циклу будемо здійснювати за допомогою оператора break, коли зустрінемо 0
    a = int(input("Задайте член послідовності (0 для завершення)"))
    if a == 0:
        break

    if a < 0:
        continue

    if a < min:
        min = a


print("Minimum =", min)